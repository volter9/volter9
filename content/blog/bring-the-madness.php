Огромное влияние на дроп в треке оказал Excision, по моему мнению (больше 
похоже на Excision чем Pegboard Nerds).

Данный трек содержит эпичное вступление которое дает ощущение джунглей. 
Агрессивный рэп после краткого вступление поддерживает атмосферу джунглей и дроп 
окончательно <!-- "I see what you did here" --> возвыщает трек на новую волну 
эфиории.

В общем, трек и вправду приносит безумие в ума слушателей.

